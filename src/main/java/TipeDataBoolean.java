public class TipeDataBoolean {
    public static void main(String[] args){

        var firstBoolean = false;
        System.out.println("firstBoolean = " +firstBoolean);

        var secondBoolean = true;
        System.out.println("secondBoolean = " +secondBoolean);

        // BOOLEAN UNTUK IF ELSE
        if (firstBoolean == true){
            System.out.println("First Boolean is " +firstBoolean);
        } else {
            System.out.println("First Boolean is " +firstBoolean);
        }

        if (secondBoolean == true){
            System.out.println("Second Boolean is = " +secondBoolean);
        } else {
            System.out.println("Second Boolean is = " +secondBoolean);
        }
    }
}
