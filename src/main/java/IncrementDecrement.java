public class IncrementDecrement {
    public static void main(String[] args){
        int a = 2;
        int b = 10;
        int c = 30;
        int d = 20;

        // INCREMENT
        System.out.println(a++);
        System.out.println(++b);
        System.out.println(c--);
        System.out.println(--d);
    }
}
