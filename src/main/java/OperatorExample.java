public class OperatorExample {

    public static void main(String[] args){

        // MEMBUAT VARIABLE DEKLARASI, DENGAN NAMA NUM
        int num;

        // MENGISI VARIABLE NUM DENGAN NILAI
        num = 100;

        // MENAMPILKAN VARIABLE NUM
        System.out.println("Nilai dari variable Num = " +num);

        // MENGALIKAN NUM DENGAN 2
        num *= 2;

        // MENAMPILKAN ULANG
        System.out.println("Nilai Terbaru Num setelah dikalikan dengan 2 = " +num);
    }
}
